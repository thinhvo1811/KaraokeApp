	public void DocDuLieuDatabaseVaoTable() {
		model.setRowCount(0);
		List<TaiKhoan> list = taiKhoan_DAO.getAllTaiKhoan();
				for (TaiKhoan tk : list) {
					Object [] row = {tk.getTenDangNhap(),tk.getMatKhau(),tk.getNhanVien().getMaNV()};
					model.addRow(row);
				}
	}
	private void xoaRongTextfields() {
		txtTenDN.setText(taiKhoan_DAO.getTenDN());
		txtMK.setText("");
		cboMaNV.setSelectedIndex(0);
	}
	private void selectedRow(int row) {
		if(row != -1) {
			tblTaiKhoan.setRowSelectionInterval(row, row);
			tblTaiKhoan.scrollRectToVisible(tblTaiKhoan.getCellRect(row, row, true));
		}
	}
	public void loadCBBNhanVien(JComboBox<String> c) {
		c.removeAllItems();
		for (NhanVien nv : nhanVien_DAO.getAllNhanVien()) {
			c.addItem(nv.getMaNV());
		}
	}
	private boolean validData() {
		String maTK = txtTenDN.getText().trim(); 

		if (!(maTK.length() > 0 && maTK.matches("TK\\d{3}"))) {
			JLabel lbl1 = new JLabel("Error: Tên đăng nhập theo mẫu: TK + Dãy gồm 3 chữ số");
			lbl1.setFont(new Font("Arial", Font.PLAIN, 18));
			JOptionPane.showMessageDialog(this, lbl1);
			txtTenDN.requestFocus();;
			return false;
		}
		return true;
	}


