    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
    	if (txtTenDN.getText().equals("") || txtMK.getText().equals("")) {
			JLabel lbl1 = new JLabel("Vui lòng nhập đầy đủ thông tin!");
			lbl1.setFont(new Font("Arial", Font.PLAIN, 18));
    		JOptionPane.showMessageDialog(this, lbl1);
		} else {
	        if(validData()) {
	        	TaiKhoan tk = new TaiKhoan(txtTenDN.getText(), txtMK.getText(), new NhanVien(cboMaNV.getSelectedItem().toString()));
				if(taiKhoan_DAO.getAllTaiKhoan().contains(tk)) {
					JLabel lbl2 = new JLabel("Không được thêm trùng tên đăng nhập");
					lbl2.setFont(new Font("Arial", Font.PLAIN, 18));
					JOptionPane.showMessageDialog(this, lbl2);
				} else {
		            taiKhoan_DAO.addTaiKhoan(tk);;
		            DocDuLieuDatabaseVaoTable();
		            xoaRongTextfields();
				}
	        }
		}
    }//GEN-LAST:event_btnCapNhatActionPerformed

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
		int row = tblTaiKhoan.getSelectedRow();
		if (row != -1) {
	    	if (txtTenDN.getText().equals("") || txtMK.getText().equals("")) {
				JLabel lbl1 = new JLabel("Vui lòng nhập đầy đủ thông tin!");
				lbl1.setFont(new Font("Arial", Font.PLAIN, 18));
	    		JOptionPane.showMessageDialog(this, lbl1);
			} else {
		        if(validData()) {
			        TaiKhoan tk = new TaiKhoan(txtTenDN.getText(), txtMK.getText(), new NhanVien(cboMaNV.getSelectedItem().toString()));
			        taiKhoan_DAO.updateTaiKhoan(tk);
			        DocDuLieuDatabaseVaoTable();
			        xoaRongTextfields();
		        }
			}
		}else {
			JLabel lbl3 = new JLabel("Bạn chưa chọn tài khoản cần cập nhật");
			lbl3.setFont(new Font("Arial", Font.PLAIN, 18));
			JOptionPane.showMessageDialog(this, lbl3);
		}
    }//GEN-LAST:event_btnCapNhatActionPerformed
    
    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
		int row = tblTaiKhoan.getSelectedRow();
		if (row != -1) {
			JLabel lbl1 = new JLabel("Bạn có chắc muốn xóa không?");
			lbl1.setFont(new Font("Arial", Font.PLAIN, 18));
			int ask = JOptionPane.showConfirmDialog(this, lbl1, "Cảnh Báo!", JOptionPane.YES_NO_OPTION);
			if(ask == JOptionPane.YES_OPTION) {
		    	taiKhoan_DAO.deleteTaiKhoan(txtTenDN.getText());
		        DocDuLieuDatabaseVaoTable();
				xoaRongTextfields();
				JLabel lbl2 = new JLabel("Xóa thành công");
				lbl2.setFont(new Font("Arial", Font.PLAIN, 18));
				JOptionPane.showMessageDialog(this, lbl2);
				if(row < taiKhoan_DAO.getAllTaiKhoan().size() - 1) {
					selectedRow(row);
				} else if(taiKhoan_DAO.getAllTaiKhoan().size() > 0) {
					row = 0;
					selectedRow(row);
				}
			}
		}else {
			JLabel lbl3 = new JLabel("Bạn chưa chọn tài khoản cần xóa");
			lbl3.setFont(new Font("Arial", Font.PLAIN, 18));
			JOptionPane.showMessageDialog(this, lbl3);
		}
}//GEN-LAST:event_btnCapNhatActionPerformed
    
