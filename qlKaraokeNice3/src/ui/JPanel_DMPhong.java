    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtMaKHActionPerformed
    	if (txtMaP.getText().equals("") || txtTenP.getText().equals("")) {
			JLabel lbl1 = new JLabel("Vui lòng nhập đầy đủ thông tin!");
			lbl1.setFont(new Font("Arial", Font.PLAIN, 18));
    		JOptionPane.showMessageDialog(this, lbl1);
		} else {
	        if(validData()) {
				boolean tt = true;
				if(cboTrangThai.getSelectedItem().equals("Đang sử dụng"))
					tt = false;
	        	Phong p = new Phong(txtMaP.getText(), txtTenP.getText(), tt, new LoaiPhong(loaiPhong_DAO.getMaLoaiPTheoTen(cboLoaiP.getSelectedItem().toString())));
				if(phong_DAO.getAllPhong().contains(p)) {
					JLabel lbl2 = new JLabel("Không được thêm trùng mã phòng");
					lbl2.setFont(new Font("Arial", Font.PLAIN, 18));
					JOptionPane.showMessageDialog(this, lbl2);
				} else {
		            phong_DAO.addPhong(p);
		            DocDuLieuDatabaseVaoTable();
		            xoaRongTextfields();
				}
	        }
		}
    }//GEN-LAST:event_txtMaKHActionPerformed

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
		int row = tblPhong.getSelectedRow();
		if (row != -1) {
	    	if (txtMaP.getText().equals("") || txtTenP.getText().equals("")) {
				JLabel lbl1 = new JLabel("Vui lòng nhập đầy đủ thông tin!");
				lbl1.setFont(new Font("Arial", Font.PLAIN, 18));
	    		JOptionPane.showMessageDialog(this, lbl1);
			} else {
		        if(validData()) {
					boolean tt = true;
					if(cboTrangThai.getSelectedItem()=="Đang sử dụng")
						tt = false;
			        Phong p = new Phong(txtMaP.getText(), txtTenP.getText(), tt,  new LoaiPhong(loaiPhong_DAO.getMaLoaiPTheoTen(cboLoaiP.getSelectedItem().toString())));
			        phong_DAO.updatePhong(p);
			        DocDuLieuDatabaseVaoTable();
			        xoaRongTextfields();
		        }
			}
		}else {
			JLabel lbl3 = new JLabel("Bạn chưa chọn phòng cần cập nhật");
			lbl3.setFont(new Font("Arial", Font.PLAIN, 18));
			JOptionPane.showMessageDialog(this, lbl3);
		}
    }//GEN-LAST:event_btnCapNhatActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboTrangThaiActionPerformed
		int row = tblPhong.getSelectedRow();
		if (row != -1) {
			JLabel lbl1 = new JLabel("Bạn có chắc muốn xóa không?");
			lbl1.setFont(new Font("Arial", Font.PLAIN, 18));
			int ask = JOptionPane.showConfirmDialog(this, lbl1, "Cảnh Báo!", JOptionPane.YES_NO_OPTION);
			if(ask == JOptionPane.YES_OPTION) {
		    	phong_DAO.deletePhong(txtMaP.getText());
		        DocDuLieuDatabaseVaoTable();
				xoaRongTextfields();
				JLabel lbl2 = new JLabel("Xóa thành công");
				lbl2.setFont(new Font("Arial", Font.PLAIN, 18));
				JOptionPane.showMessageDialog(this, lbl2);
				if(row < phong_DAO.getAllPhong().size() - 1) {
					selectedRow(row);
				} else if(phong_DAO.getAllPhong().size() > 0) {
					row = 0;
					selectedRow(row);
				}
			}
		}else {
			JLabel lbl3 = new JLabel("Bạn chưa chọn phòng cần xóa");
			lbl3.setFont(new Font("Arial", Font.PLAIN, 18));
			JOptionPane.showMessageDialog(this, lbl3);
		}
    }//GEN-LAST:event_cboTrangThaiActionPerformed
    
