	public void DocDuLieuDatabaseVaoTable() {
		model.setRowCount(0);
		DecimalFormat formatter = new DecimalFormat("###,###,###");
		List<Phong> list = phong_DAO.getAllPhong();
				for (Phong p : list) {	
					String tt = "Trống";
					if(p.getTrangThai()==false)
						tt="Đang sử dụng";	
					Object [] row = {p.getMaPhong(),p.getTenPhong(),p.getLoaiPhong().getTenloai(),p.getLoaiPhong().getSoNguoiToiDa(),formatter.format(p.getLoaiPhong().getGiaPhong()),tt};
					model.addRow(row);
				}
	}

	public void loadCBBLoaiP(JComboBox<String> c) {
		c.removeAllItems();
		for (LoaiPhong lp : loaiPhong_DAO.getAllLoaiPhong()) {
			c.addItem(lp.getTenloai());
		}
	}
	private void xoaRongTextfields() {
        txtMaP.setText(phong_DAO.getMaPhong());
		txtTenP.setText("");
		cboLoaiP.setSelectedIndex(0);
		cboTrangThai.setSelectedIndex(0);
	}
	private void selectedRow(int row) {
		if(row != -1) {
			tblPhong.setRowSelectionInterval(row, row);
			tblPhong.scrollRectToVisible(tblPhong.getCellRect(row, row, true));
		}
	}
	private boolean validData() {
		String maP = txtMaP.getText().trim(); 

		if (!(maP.length() > 0 && maP.matches("P\\d{3}"))) {
			JLabel lbl1 = new JLabel("Error: Mã phòng theo mẫu: P + Dãy gồm 3 chữ số");
			lbl1.setFont(new Font("Arial", Font.PLAIN, 18));
			JOptionPane.showMessageDialog(this, lbl1);
			txtMaP.requestFocus();;
			return false;
		}
		return true;
	}


