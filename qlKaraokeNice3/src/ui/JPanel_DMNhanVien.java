	private void selectedRow(int row) {
		if(row != -1) {
			tblNhanVien.setRowSelectionInterval(row, row);
			tblNhanVien.scrollRectToVisible(tblNhanVien.getCellRect(row, row, true));
		}
	}
	private boolean validData() {
		String maNV = txtMaNV.getText().trim(), soCMND = txtCMND.getText().trim(), soDT = txtSDT.getText().trim(); 
		Date ngaySinh = dchNgaySinh.getDate();
		long millis=System.currentTimeMillis();
		
		if (!(maNV.length() > 0 && maNV.matches("NV\\d{3}"))) {
			JLabel lbl1 = new JLabel("Error: Mã nhân viên theo mẫu: NV + Dãy gồm 3 chữ số");
			lbl1.setFont(new Font("Arial", Font.PLAIN, 18));
			JOptionPane.showMessageDialog(this, lbl1);
			txtMaNV.requestFocus();;
			return false;
		}
		if (!(soCMND.length() > 0 && soCMND.matches("[0-9]{9}"))) {
			JLabel lbl2 = new JLabel("Error: Số CMND không hợp lệ");
			lbl2.setFont(new Font("Arial", Font.PLAIN, 18));
			JOptionPane.showMessageDialog(this, lbl2);
			txtCMND.requestFocus();;
			return false;
		}
		if (!(soDT.length() > 0 && soDT.matches("^(0|\\+84)(\\s|\\.)?((3[2-9])|(5[689])|(7[06-9])|(8[1-689])|(9[0-46-9]))(\\d)(\\s|\\.)?(\\d{3})(\\s|\\.)?(\\d{3})$"))) {
			JLabel lbl3 = new JLabel("Error: Số điện thoại không hợp lệ");
			lbl3.setFont(new Font("Arial", Font.PLAIN, 18));
			JOptionPane.showMessageDialog(this, lbl3);
			txtSDT.requestFocus();;
			return false;
		}
		if(ngaySinh.after(new java.util.Date(millis))){
			JLabel lbl4 = new JLabel("Error: Ngày sinh không hợp lệ");
			lbl4.setFont(new Font("Arial", Font.PLAIN, 18));
			JOptionPane.showMessageDialog(this, lbl4);
			return false;
		}
		return true;
	}

