    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
		java.util.Date datetime = dchNgaySinh.getDate();
    	if (txtMaNV.getText().equals("") || txtTenNV.getText().equals("") || datetime == null ||
				txtCMND.getText().equals("") || txtSDT.getText().equals("") || txtDiaChi.getText().equals("")) {
			JLabel lbl1 = new JLabel("Vui lòng nhập đầy đủ thông tin!");
			lbl1.setFont(new Font("Arial", Font.PLAIN, 18));
    		JOptionPane.showMessageDialog(this, lbl1);
		} else {
	        if(validData()) {
				boolean gt = true;
				if(cboGioiTinh.getSelectedItem().equals("Nữ"))
					gt = false;
				java.sql.Date ngaySinh = new java.sql.Date(datetime.getTime());
	        	NhanVien nv = new NhanVien(txtMaNV.getText(), txtTenNV.getText(), gt, ngaySinh, txtCMND.getText(), txtSDT.getText(), txtDiaChi.getText(), new ChucVu(chucVu_DAO.getMaCVTheoTen(cboChucVu.getSelectedItem().toString())));
				if(nhanVien_DAO.getAllNhanVien().contains(nv)) {
					JLabel lbl2 = new JLabel("Không được thêm trùng mã nhân viên");
					lbl2.setFont(new Font("Arial", Font.PLAIN, 18));
					JOptionPane.showMessageDialog(this, lbl2);
				} else {
		            nhanVien_DAO.addNhanVien(nv);
		            DocDuLieuDatabaseVaoTable();
		            xoaRongTextfields();
				}
	        }
		}
    }//GEN-LAST:event_btnCapNhatActionPerformed
    
