    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
    	String tt;
    	if(cboTrangThai.getSelectedItem().equals("Đang sử dụng"))
    		tt = "0";
    	else if(cboTrangThai.getSelectedIndex()==0)
    		tt = "";
    	else
    		tt = "1";
    	String maP = txtMaP.getText(), tenP = txtTenP.getText(),
    			loaiP = cboLoaiP.getSelectedItem().toString();
    	if(cboLoaiP.getSelectedIndex()==0)
    		loaiP = "";
    	model.setRowCount(0);
		DecimalFormat formatter = new DecimalFormat("###,###,###");
		ArrayList<Phong> dsP = phong_DAO.getPhong(maP, tenP, loaiP, tt);
		for(Phong p : dsP) {
			String trangThai = "Đang sử dụng";
			if(p.getTrangThai()==true)
				trangThai="Trống";
			model.addRow(new Object[] { p.getMaPhong(),p.getTenPhong(),p.getLoaiPhong().getTenloai(),p.getLoaiPhong().getSoNguoiToiDa(),formatter.format(p.getLoaiPhong().getGiaPhong())
					,trangThai});
		}
    }//GEN-LAST:event_btnTimKiemActionPerformed

    
	public void DocDuLieuDatabaseVaoTable() {
		model.setRowCount(0);
		DecimalFormat formatter = new DecimalFormat("###,###,###");
		List<Phong> list = phong_DAO.getAllPhong();
				for (Phong p : list) {	
					String tt = "Trống";
					if(p.getTrangThai()==false)
						tt="Đang sử dụng";	
					Object [] row = {p.getMaPhong(),p.getTenPhong(),p.getLoaiPhong().getTenloai(),p.getLoaiPhong().getSoNguoiToiDa(),formatter.format(p.getLoaiPhong().getGiaPhong()),tt};
					model.addRow(row);
				}
	}

	public void loadCBBLoaiP(JComboBox<String> c) {
		c.removeAllItems();
		c.addItem("Loại Phòng");
		for (LoaiPhong lp : loaiPhong_DAO.getAllLoaiPhong()) {
			c.addItem(lp.getTenloai());
		}
	}
